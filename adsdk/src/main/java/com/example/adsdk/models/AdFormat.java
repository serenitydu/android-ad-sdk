package com.example.adsdk.models;

/**
 * Enum representing different ad content formats supported by the SDK
 */
public enum AdFormat {
    /**
     * Text-based ad with customizable text and background color
     */
    TEXT,

    /**
     * Image-based ad loaded from URL or local resource
     */
    IMAGE,

    /**
     * HTML-based ad rendered in a WebView
     */
    HTML
}
